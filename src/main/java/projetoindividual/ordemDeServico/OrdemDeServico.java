package projetoindividual.ordemDeServico;

public class OrdemDeServico {

	private String numeroOs;
	private String idCliente;
	private String tipoEquipamento;
	private String marcaEquipamento;
	private String corEquipamento;
	private String modeloEquipamento;
	private String serialEquipamento;
	private String solicitacao;
	private String condicoesEquipamento;
	private String senhaEquipamento;
	private String observacoesInternas;
	
	public String getNumeroOs() {
		return numeroOs;
	}
	public void setNumeroOs(String numeroOs) {
		this.numeroOs = numeroOs;
	}
	public String getIdCliente() {
		return idCliente;
	}
	public void setIdCliente(String idCliente) {
		this.idCliente = idCliente;
	}
	public String getTipoEquipamento() {
		return tipoEquipamento;
	}
	public void setTipoEquipamento(String tipoEquipamento) {
		this.tipoEquipamento = tipoEquipamento;
	}
	public String getMarcaEquipamento() {
		return marcaEquipamento;
	}
	public void setMarcaEquipamento(String marcaEquipamento) {
		this.marcaEquipamento = marcaEquipamento;
	}
	public String getCorEquipamento() {
		return corEquipamento;
	}
	public void setCorEquipamento(String corEquipamento) {
		this.corEquipamento = corEquipamento;
	}
	public String getModeloEquipamento() {
		return modeloEquipamento;
	}
	public void setModeloEquipamento(String modeloEquipamento) {
		this.modeloEquipamento = modeloEquipamento;
	}
	public String getSerialEquipamento() {
		return serialEquipamento;
	}
	public void setSerialEquipamento(String serialEquipamento) {
		this.serialEquipamento = serialEquipamento;
	}
	public String getSolicitacao() {
		return solicitacao;
	}
	public void setSolicitacao(String solicitacao) {
		this.solicitacao = solicitacao;
	}
	public String getCondicoesEquipamento() {
		return condicoesEquipamento;
	}
	public void setCondicoesEquipamento(String condicoesEquipamento) {
		this.condicoesEquipamento = condicoesEquipamento;
	}
	public String getSenhaEquipamento() {
		return senhaEquipamento;
	}
	public void setSenhaEquipamento(String senhaEquipamento) {
		this.senhaEquipamento = senhaEquipamento;
	}
	public String getObservacoesInternas() {
		return observacoesInternas;
	}
	public void setObservacoesInternas(String observacoesInternas) {
		this.observacoesInternas = observacoesInternas;
	}
	
}
